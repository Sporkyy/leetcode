/**
 * Runtime: 88 ms, faster than 74.47% of JavaScript online submissions
 * Memory Usage: 40.4 MB, less than 66.67% of JavaScript online submissions
 *
 * @param {string} s
 * @param {number} numRows
 * @return {string}
 */
const convert = (s, numRows) => {
  const seq = [...new Array(numRows).keys()];
  seq.push(...seq.slice(1, -1).reverse());
  const rows = new Array(numRows).fill('');
  for (let i = 0; i < s.length; i++) rows[seq[i % seq.length]] += s[i];
  return rows.join('');
};

